    private void TabMaisonEdition() {
       
        Connect();
        String colonne[]={ "ID MaisonEdition","Nom", "Contact" , "Email"};
        DefaultTableModel model = new DefaultTableModel(null, colonne);
        try {
           Statement st = conn.createStatement();
           rst = st.executeQuery("Select *from maisonedition");
            while (rst.next()) {
                Object o[] = {
                    rst.getString("idmaisonedition"),
                    rst.getString("nom"),
                    rst.getString("contact"),
                    rst.getString("email")
                };
                    model.addRow(o);
            }
            rst.close();
            conn.close();
            TabMaisonEdition.setModel(model);
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

