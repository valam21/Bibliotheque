    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        
         String nomValue = name.getText();
    String contactValue = con.getText();
    String emailValue = email.getText();
    String idMaisonEditionValue = idmaisonedition.getText();

    // Vérifier si l'un des champs obligatoires est vide
    if (nomValue.isEmpty() || contactValue.isEmpty() || emailValue.isEmpty() || idMaisonEditionValue.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Veuillez remplir tous les champs.", "Erreur", JOptionPane.ERROR_MESSAGE);
        return; // Sortir de la méthode sans exécuter la requête SQL
    }

    // Tous les champs obligatoires sont remplis, exécuter la requête SQL
    String sql = "update maisonedition set nom = ?, contact = ?, email = ? where idmaisonedition = ?";
    try {
            Connect();
            stm = conn.prepareStatement(sql);
            stm.setString(1, nomValue);
            stm.setString(2, contactValue);
            stm.setString(3, emailValue);
            stm.setString(4, idMaisonEditionValue);
            stm.executeUpdate();
            stm.close();
            conn.close();
            JOptionPane.showMessageDialog(null, "Modification effectuée avec succès.");
            TabMaisonEdition(); // Assurez-vous que cette méthode existe pour actualiser l'affichage des données après la modification
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_updateActionPerformed

