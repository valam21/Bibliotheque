    private void TabStock() {
         
         
        Connect();
        String colonne[]={ "ID Stock","Quantité", "Date Stock", "ID Livre" , "ID Emplacement"};
        DefaultTableModel model = new DefaultTableModel(null, colonne);
        try {
           Statement st = conn.createStatement();
           rst = st.executeQuery("Select *from stocker");
            while (rst.next()) {
                Object o[] = {
                    rst.getString("idstocker"),
                    rst.getString("qtestock"),
                    rst.getString("datestock"),
                    rst.getString("idlivre"),
                    rst.getString("idemplacement")
                };
                    model.addRow(o);
            }
            rst.close();
            conn.close();
            TabStock.setModel(model);
            
        } catch (Exception e) {
            e.printStackTrace();
        }
     
    }
