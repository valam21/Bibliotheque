    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
      
String qtiteValue = qtité.getText();
        String dateValue = date.getText();
        String idLivreValue = idlivre.getText();
        String idEmplacementValue = idempla.getText();
        String idStockerValue = idstock.getText();

    
    if (qtiteValue.isEmpty() || dateValue.isEmpty() || idLivreValue.isEmpty() || idEmplacementValue.isEmpty() || idStockerValue.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Veuillez remplir tous les champs.", "Erreur", JOptionPane.ERROR_MESSAGE);
        return; 
    }

    
    String sql = "update stocker set qtestock = ?, datestock = ?, idlivre = ?, idemplacement = ? where idstocker = ?";
    try {
        Connect();
        stm = conn.prepareStatement(sql);
        stm.setString(1, qtiteValue);
        stm.setString(2, dateValue);
        stm.setString(3, idLivreValue);
        stm.setString(4, idEmplacementValue);
        stm.setString(5, idStockerValue);
        stm.executeUpdate();
        stm.close();
        conn.close();
        JOptionPane.showMessageDialog(null, "Modification effectuée avec succès.");
        TabStock(); 
    } catch (SQLException e) {
        e.printStackTrace();
    }
    }//GEN-LAST:event_updateActionPerformed

