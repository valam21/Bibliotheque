    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        
          String dateEmpruntValue = dateE.getText();
    String dateRetourValue = dateR.getText();
    String idLivreValue = idbook.getText();
    String idAbonneValue = idabonne.getText();
    String idEmpruntValue = idemprunt.getText();

    // Vérifier si l'un des champs obligatoires est vide
    if (dateEmpruntValue.isEmpty() || dateRetourValue.isEmpty() || idLivreValue.isEmpty() || idAbonneValue.isEmpty() || idEmpruntValue.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Veuillez remplir tous les champs.", "Erreur", JOptionPane.ERROR_MESSAGE);
        return; // Sortir de la méthode sans exécuter la requête SQL
    }

    // Tous les champs obligatoires sont remplis, exécuter la requête SQL
    String sql = "update emprunter set dateemprunte = ?, dateretour = ?, idlivre = ?, idabonnee = ? where idemprunter = ?";
    try {
            Connect();
            stm = conn.prepareStatement(sql);
            stm.setString(1, dateEmpruntValue);
            stm.setString(2, dateRetourValue);
            stm.setString(3, idLivreValue);
            stm.setString(4, idAbonneValue);
            stm.setString(5, idEmpruntValue);
            stm.executeUpdate();
            stm.close();
            conn.close();
            JOptionPane.showMessageDialog(null, "Modification effectuée avec succès.");
            TabEmprunt(); // Assurez-vous que cette méthode existe pour actualiser l'affichage des données après la modification
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_updateActionPerformed

