    private void TabAuteur() {
        
        Connect();
        String colonne[]={ "Idauteur","nom", "Prenom", "Contact"};
        DefaultTableModel model = new DefaultTableModel(null, colonne);
        try {
           Statement st = (Statement) conn.createStatement();
           rst = st.executeQuery("Select *from auteur");
            while (rst.next()) {
                Object o[] = {
                    rst.getString("idauteur"),
                    rst.getString("nom"),
                    rst.getString("prenom"),
                    rst.getString("contact")            
                };
                    model.addRow(o);
            }
            rst.close();
            conn.close();
            TabAuteur.setModel(model);
            
        } catch (Exception e) {
            e.printStackTrace();
        }
     
    }

