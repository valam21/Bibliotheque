    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
          String nomValue = name.getText();
    String prenomValue = pre.getText();
    String contactValue = con.getText();
    String idauteurValue = idauteur.getText();

    // Vérifier si l'un des champs est vide
    if (nomValue.isEmpty() || prenomValue.isEmpty() || contactValue.isEmpty() || idauteurValue.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Veuillez remplir tous les champs avant de modifier.", "Erreur", JOptionPane.ERROR_MESSAGE);
        return; // Sortir de la méthode sans exécuter la requête SQL
    }

    // Les champs ne sont pas vides, exécuter la requête SQL
    String sql = "update auteur set nom = ?, prenom = ?, contact = ? where idauteur = ?";
    try {
            Connect();
            stm = conn.prepareStatement(sql);
            stm.setString(1, nomValue);
            stm.setString(2, prenomValue);
            stm.setString(3, contactValue);
            stm.setString(4, idauteurValue);
            stm.executeUpdate();
            stm.close();
            conn.close();
            JOptionPane.showMessageDialog(null, "Modification effectuée avec succès.");
            TabAuteur(); // Assurez-vous que cette méthode existe pour actualiser l'affichage des données après la modification
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_updateActionPerformed

